class CoffeeType {
  final String name;
  final String description;
  final String price;
  final String image;
  final String rating;
  final String voiceAmount;
  final String roastType;

  CoffeeType({
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.rating,
    required this.voiceAmount,
    required this.roastType,
  });
}
